package Interview_Question;

public class SubStringClass {
    public static void main(String[] args) {
        String ss = "Hello";
        System.out.println(ss);
        System.out.println(ss.substring(0,3));  // this is start with zero index and ending with 3 index.
        System.out.println(ss.substring(2)); // this is starting with zero index
    }
}
